import {
  existsSync,
  mkdirSync,
  readdirSync,
  readFileSync,
  rmSync,
  writeFileSync,
} from "node:fs";
import { join } from "node:path";

const pkgRoot = "packages";
const changesetDir = ".changeset";

function getPublishablePackages() {
  if (!existsSync(pkgRoot)) return [];

  const result = [];
  for (const dir of readdirSync(pkgRoot)) {
    const pkgPath = join(pkgRoot, dir, "package.json");
    if (!existsSync(pkgPath)) continue;

    const pkg = JSON.parse(readFileSync(pkgPath, "utf8"));
    if (!pkg?.name || pkg.private) continue;

    result.push({ name: pkg.name, version: pkg.version });
  }

  return result.sort((a, b) => a.name.localeCompare(b.name));
}

const packages = getPublishablePackages();
if (packages.length === 0) {
  console.log("No publishable packages found under packages/*.");
  process.exit(0);
}

if (!existsSync(changesetDir)) {
  mkdirSync(changesetDir, { recursive: true });
}

const stamp = new Date().toISOString().slice(0, 10).replaceAll("-", "");
const fileName = `reset-baseline-${stamp}.md`;
const filePath = join(changesetDir, fileName);
const hadExistingBaseline = existsSync(filePath);

const staleBaselineFiles = readdirSync(changesetDir)
  .filter((name) => /^reset-baseline-\d{8}\.md$/.test(name))
  .filter((name) => name !== fileName);

for (const stale of staleBaselineFiles) {
  rmSync(join(changesetDir, stale));
}

const frontmatter = packages.map((pkg) => `"${pkg.name}": major`).join("\n");
const versionLines = packages
  .map((pkg) => `- ${pkg.name}: v${pkg.version} -> next major`)
  .join("\n");
const body = [
  "---",
  frontmatter,
  "---",
  "",
  "<!-- generated by scripts/reset-release-baseline.mjs -->",
  "",
  "Baseline re-release: start a new major release line for all publishable packages.",
  "",
  "Current package versions used to generate this baseline:",
  versionLines,
  "",
  "Notes:",
  "- This does not rewrite npm history or delete previously published versions.",
  "- It creates a clean new major baseline on top of current packages.",
].join("\n");

writeFileSync(filePath, body, "utf8");
console.log(`${hadExistingBaseline ? "Updated" : "Created"} ${filePath}`);
if (staleBaselineFiles.length > 0) {
  console.log(`Removed stale baseline files: ${staleBaselineFiles.join(", ")}`);
}
